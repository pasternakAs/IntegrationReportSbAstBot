namespace IntegrationReportSbAstBot.Class
{
    /// <summary>
    /// Настройки конфигурации Telegram бота
    /// Содержит параметры управления доступом, состоянием и сообщениями бота
    /// Используется для централизованного управления поведением бота через конфигурацию
    /// </summary>
    public class BotSettings
    {
        /// <summary>
        /// Состояние включения/выключения бота
        /// Управляет общей доступностью функциональности бота для всех пользователей
        /// </summary>
        /// <value>True по умолчанию - бот включен</value>
        /// <remarks>
        /// True - бот полностью функционален и обрабатывает запросы
        /// False - бот отклоняет все запросы и отображает сообщение MaintenanceMessage
        /// </remarks>
        public bool IsEnabled { get; set; } = true;

        /// <summary>
        /// Список идентификаторов пользователей Telegram с правами администратора
        /// Эти пользователи имеют расширенные привилегии для управления ботом и авторизацией
        /// </summary>
        /// <example>[6426583094, 123456789, 987654321]</example>
        /// <remarks>
        /// Администраторы могут:
        /// - Включать/выключать бота
        /// - Одобрять запросы на авторизацию
        /// - Просматривать список запросов
        /// - Управлять доступом других пользователей
        /// </remarks>
        public List<long> AdminUserIds { get; set; } = new();

        /// <summary>
        /// Сообщение, отображаемое неавторизованным пользователям
        /// Используется когда пользователь пытается получить доступ к функциям бота без авторизации
        /// </summary>
        /// <value>"❌ Доступ запрещен. Обратитесь к администратору." по умолчанию</value>
        /// <example>"❌ Доступ запрещен. Обратитесь к администратору."</example>
        public string UnauthorizedMessage { get; set; } = "❌ Доступ запрещен. Обратитесь к администратору.";

        /// <summary>
        /// Сообщение, отображаемое всем пользователям когда бот выключен
        /// Используется для информирования о технических работах или плановом обслуживании
        /// </summary>
        /// <value>"❌ Бот временно недоступен. Технические работы." по умолчанию</value>
        /// <example>"❌ Бот временно недоступен. Технические работы."</example>
        public string MaintenanceMessage { get; set; } = "❌ Бот временно недоступен. Технические работы.";
    }
}